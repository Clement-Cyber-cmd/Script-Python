def additionner(a, b):
    resultat = a + b
    return resultat
somme = additionner(5,3)
print(f"Le resultat de la somme est {somme}")
nouvelle_somme = additionner(10, 2) * 2
print(f"Le nouveau calcul donne : {nouvelle_somme}")

def soustraire(a, b):
    resultat = a - b
    return resultat
différence = soustraire(5,3)
print(f"Le resultat de la différence est {différence}")
nouvelle_différence = soustraire(10, 2) * 2
print(f"Le nouveau calcul donne : {nouvelle_différence}")

